def cpp_escape(text: str) -> str:
    out = []
    for ch in text:
        if ch == "\\":
            out.append("\\\\")
        elif ch == '"':
            out.append('\\"')
        elif ch == "\n":
            out.append("\\n")
        elif ch == "\t":
            out.append("\\t")
        else:
            assert 32 <= ord(ch) < 127, f"non-printable {ch!r} needs escaping"
            out.append(ch)
    return "".join(out)


def literal(text: str, indent: str) -> str:
    """One C string literal per source line, for readable diffs."""
    lines = text.split("\n")
    parts = [line + "\n" for line in lines[:-1]]
    if lines[-1]:
        parts.append(lines[-1])
    if not parts:
        return indent + '""'
    return "\n".join(f'{indent}"{cpp_escape(part)}"' for part in parts)


def main() -> None:
    here = pathlib.Path(__file__).resolve().parent
    chunks = [
        "// GENERATED by tests/gen_expected.py (make parity-cases) - do not edit.\n"
        "// Each expectation is REAL CPython stdout ("
        + f"this run: {sys.version.split()[0]}"
        + "), checked in\n"
        "// so CI never needs a Python; parity.cpp static_asserts ctpy against it.\n"
    ]
    for name, source in CASES:
        result = subprocess.run(
            [sys.executable, "-c", source], capture_output=True, text=True, timeout=30
        )
        if result.returncode != 0:
            sys.exit(f"case {name!r} failed under CPython:\n{result.stderr}")
        chunks.append(
            f"\nPARITY_CASE({name},\n{literal(source, '\t')},\n{literal(result.stdout, '\t')})\n"
        )
    (here / "parity_cases.inc").write_text("".join(chunks))
    print(f"wrote {here / 'parity_cases.inc'} ({len(CASES)} cases)")


if __name__ == "__main__":
    main()
