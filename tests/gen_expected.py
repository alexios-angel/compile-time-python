#!/usr/bin/env python3
"""Regenerate tests/parity_cases.inc from CASES by running every snippet
under the system CPython and capturing its stdout.

Offline dev tool (family convention: CI stays hermetic - the generated
.inc is checked in; re-run via `make parity-cases` after editing CASES
or widening the interpreter subset). Each snippet must stay inside the
ctpy v0.1 subset AND print nothing whose spelling ctpy documents as
divergent (bignum overflow, 17-significant-digit float reprs, set
display order), so that

    static_assert(ctpy::run<SRC>().stdout() == EXPECTED);

pins true CPython parity at compile time.
"""

